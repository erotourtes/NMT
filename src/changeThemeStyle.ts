
export default function changeThemeStyle() {
    document.documentElement.style.setProperty("--main-color", "#6bde3b");
    document.documentElement.style.setProperty("--primary-color", "#6bde3b");
    document.documentElement.style.setProperty("--sub-color", "#1f5e6b");
    document.documentElement.style.setProperty("--text-color", "#e9efe6");
    document.documentElement.style.setProperty("--error-color", "#ca4754");
    document.documentElement.style.setProperty("--bg-color", "#1c292f");
    document.documentElement.style.setProperty("--sub-alt-color", "#1b3238");
}